package ProgrammingProject.model;

/**
 * Represents the possible states of a line in the Dots and Boxes game.
 * This enumeration defines three possible states for a line on the game board:
 * EMPTY, indicating that the line has not been drawn yet,
 * BLUE, representing lines drawn by the Blue player,
 * and RED, representing lines drawn by the Red player.
 */
public enum Line {

    /**
     * Indicates that the line has not been drawn yet.
     */
    EMPTY,
    /**
     * Represents a line drawn by the Blue player.
     */
    BLUE,
    /**
     * Represents a line drawn by the Red player.
     */
    RED;


}
