package ProgrammingProject.model;

/**
 * Represents the possible states of a line in the Dots and Boxes game.
 * A line can be in one of three states: EMPTY, BLUE, or RED.
 * EMPTY indicates that the line has not been drawn yet.
 * BLUE and RED represent lines drawn by the respective players.
 */
public enum Line {

    EMPTY, // Line has not been drawn
    BLUE, // Line drawn by the Blue player
    RED; // Line drawn by the Red player


}
